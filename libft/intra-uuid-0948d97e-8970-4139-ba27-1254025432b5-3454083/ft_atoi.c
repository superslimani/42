#include "libft.h"

int	ft_atoi(const char *str)
{
	int			sign;
	long int	conv;

	conv = 0;
	sign = 1;
	while (*str == ' ' || *str == '\t' || *str == '\n' ||
			*str == '\v' || *str == '\f' || *str == '\r')
		str++;
	if (*str == '-')
	{
		sign = -1;
		str++;
	}
	else if (*str == '+')
		str++;
	while (*str >= '0' && *str <= '9')
	{
		conv *= 10;
		conv += *str - '0';
		str++;
	}
	return (sign * conv);
}
